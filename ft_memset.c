#include <stddef.h>

void	*ft_memset(void *b, int c, size_t len)
{
	char	*tab;
	size_t	i;

	i = 0;
	tab = (char *)b;
	while (i < len)
	{
		tab[i] = c;
		i++;
	}
	return (b);
}
